    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (txtNumero.getText().trim().equals("")) {
            ArrayList<Computador> listaRetorno
                    = TabelaComputadoresDAO.listar();

            //Percorrer a lista e adicionar Ã  tabela
            DefaultTableModel modelo
                    = (DefaultTableModel) jTable1.getModel();

            //Limpar a tabela
            modelo.setRowCount(0);

            for (Computador item : listaRetorno) {
                modelo.addRow(new String[]{
                    String.valueOf(item.getIdComputador()),
                    String.valueOf(item.getMarcaNome()),
                    String.valueOf(item.getHD()),
                    String.valueOf(item.getProcessador())
                });
            }//fim do foreach
        } else {
            //Chamar a DAO passando o numero da nota
            String numero = txtNumero.getText();

            //TODO: Chamar a DAO e alimentar a tabela
            ArrayList<Computador> lista = TabelaComputadoresDAO.buscarPorNumero(numero);

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);

            for (Computador item : lista) {
                modelo.addRow(new String[]{
                    String.valueOf(item.getIdComputador()),
                    String.valueOf(item.getMarcaNome()),
                    String.valueOf(item.getHD()),
                    String.valueOf(item.getProcessador())
                });

            }

        }


    }//GEN-LAST:event_btnBuscarActionPerformed

