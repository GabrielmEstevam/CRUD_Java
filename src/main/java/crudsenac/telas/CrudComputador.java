    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        //Resgatar dados da linha
        int linhaSelecionada = jTable1.getSelectedRow();

        //Resgatar os dados das colunas
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        if (linhaSelecionada >= 0) {
            int idSelecionado
                    = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0)
                            .toString());

            String numeroSelecionado
                    = (modelo.getValueAt(linhaSelecionada, 1)
                            .toString());

            String valorSelecionado
                    = (modelo.getValueAt(linhaSelecionada, 2)
                            .toString());
            String processadorSelecionado = (modelo.getValueAt(linhaSelecionada, 3)
                            .toString());

            //Passar valores para o objeto
            Computador objAlterar = new Computador(
                    idSelecionado,
                    numeroSelecionado,
                    valorSelecionado,
            processadorSelecionado);

            //Enviar o objeto para a tela de cadastro
            ConsultaComputador telaAlterar = new ConsultaComputador(objAlterar);
            telaAlterar.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Selecione uma linha!");
        }


    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        int linhaSelecionada = jTable1.getSelectedRow();

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        int idExcluir
                = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());

        //Chamar a DAO para excluir
        boolean retorno = TabelaComputadoresDAO.excluir(idExcluir);

        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Sucesso!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Falha!");
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

